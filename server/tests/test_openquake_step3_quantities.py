"""STEP-3 OpenQuake registry quantities: hazard curves + UHS (ScalarField) + physics.

Covers:
  - assemble_build_spec merges validated advanced_physics + is byte-identical
    when None + typed OQ_PHYSICS_INVALID;
  - the UHS flag flips on only under GRACE2_OPENQUAKE_REGISTRY_QUANTITIES;
  - parse_hazard_curve_csv / parse_uhs_csv extract summary scalars;
  - publish_openquake_quantities routes the curve + UHS scalars into manifest
    metrics (no layer) via the shared executor.
"""

from __future__ import annotations

import pytest

from grace2_agent.workflows import postprocess_openquake as oq
from grace2_agent.workflows.model_seismic_hazard_scenario import (
    OpenQuakeWorkflowError,
    assemble_build_spec,
)
from grace2_contracts.openquake_contracts import OpenQuakeRunArgs

_BBOX = (-122.5, 37.0, -122.0, 37.5)


def _args(**kw) -> OpenQuakeRunArgs:
    return OpenQuakeRunArgs(bbox=_BBOX, **kw)


# --------------------------------------------------------------------------- #
# build_spec physics merge + UHS gating
# --------------------------------------------------------------------------- #
def test_build_spec_physics_none_byte_identical(monkeypatch) -> None:
    monkeypatch.delenv("GRACE2_OPENQUAKE_REGISTRY_QUANTITIES", raising=False)
    spec = assemble_build_spec(_args())
    assert "truncation_level" not in spec
    assert "uniform_hazard_spectra" not in spec


def test_build_spec_merges_validated_physics(monkeypatch) -> None:
    monkeypatch.delenv("GRACE2_OPENQUAKE_REGISTRY_QUANTITIES", raising=False)
    spec = assemble_build_spec(
        _args(advanced_physics={"truncation_level": 4.0,
                                "rupture_mesh_spacing_km": 2.0})
    )
    assert spec["truncation_level"] == 4.0
    assert spec["rupture_mesh_spacing_km"] == 2.0


def test_build_spec_invalid_physics_raises_typed(monkeypatch) -> None:
    monkeypatch.delenv("GRACE2_OPENQUAKE_REGISTRY_QUANTITIES", raising=False)
    with pytest.raises(OpenQuakeWorkflowError) as ei:
        assemble_build_spec(_args(advanced_physics={"bogus": 1.0}))
    assert ei.value.error_code == "OQ_PHYSICS_INVALID"


def test_build_spec_uhs_flag_gated_by_env(monkeypatch) -> None:
    monkeypatch.setenv("GRACE2_OPENQUAKE_REGISTRY_QUANTITIES", "1")
    spec = assemble_build_spec(_args())
    assert spec["uniform_hazard_spectra"] is True


# --------------------------------------------------------------------------- #
# CSV parsers
# --------------------------------------------------------------------------- #
def test_parse_hazard_curve_csv() -> None:
    text = (
        "# generated by OpenQuake\n"
        "lon,lat,depth,poe-0.1,poe-0.2,poe-0.5\n"
        "-122.4,37.4,0.0,0.8,0.5,0.1\n"
        "-122.3,37.3,0.0,0.6,0.3,0.05\n"
    )
    out = oq.parse_hazard_curve_csv(text)
    assert out["hazard_curve_imls_g"] == [0.1, 0.2, 0.5]
    assert out["hazard_curve_n_sites"] == 2
    assert out["hazard_curve_mean_poe"][0] == pytest.approx(0.7)


def test_parse_uhs_csv() -> None:
    text = (
        "# generated by OpenQuake\n"
        "lon,lat,depth,0.1~PGA,0.1~SA(0.2),0.1~SA(1.0)\n"
        "-122.4,37.4,0.0,0.3,0.6,0.2\n"
    )
    out = oq.parse_uhs_csv(text)
    assert out["uhs_periods_s"] == [0.0, 0.2, 1.0]
    assert out["uhs_mean_sa_g"][1] == pytest.approx(0.6)
    assert out["uhs_n_sites"] == 1


# --------------------------------------------------------------------------- #
# publish_openquake_quantities -> ScalarField -> metrics (no layer)
# --------------------------------------------------------------------------- #
def test_publish_openquake_quantities_scalars_to_metrics() -> None:
    captured = {}

    def _registrar(manifest, *, run_id, bbox=None):
        captured["manifest"] = manifest
        return manifest

    curve_csv = (
        "lon,lat,depth,poe-0.1,poe-0.2\n"
        "-122.4,37.4,0.0,0.8,0.5\n"
    )
    uhs_csv = (
        "lon,lat,depth,0.1~SA(0.2),0.1~SA(1.0)\n"
        "-122.4,37.4,0.0,0.6,0.2\n"
    )
    oq.publish_openquake_quantities(
        run_id="R1",
        register_manifest_layers=_registrar,
        hazard_curve_csv_text=curve_csv,
        uhs_csv_text=uhs_csv,
    )
    manifest = captured["manifest"]
    # scalars route to metrics, NO layers.
    assert manifest.layers == []
    assert "hazard_curve_imls_g" in manifest.metrics
    assert "uhs_periods_s" in manifest.metrics


def test_publish_openquake_quantities_missing_csv_is_empty() -> None:
    captured = {}

    def _registrar(manifest, *, run_id, bbox=None):
        captured["manifest"] = manifest
        return manifest

    oq.publish_openquake_quantities(
        run_id="R", register_manifest_layers=_registrar,
        hazard_curve_csv_text=None, uhs_csv_text=None,
    )
    assert captured["manifest"].layers == []
    assert captured["manifest"].metrics == {}
